package openGLDemos;

public class Ball {
	
	double x = 0;
	double y = 0;
	double z = 0;
	double xadd = 0;
	double yadd = 0;
	double zadd = 0;
	
	public Ball(double x, double y , double z, double xadd, double yadd, double zadd){
		this.x=x;
		this.y=y;
		this.z=z;
		this.xadd = xadd;
		this.yadd=yadd;
		this.zadd=zadd;
	}

}

